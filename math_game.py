def add(num_1, num_2):
    result = num_1 + num_2
    print(f'{num_1} + {num_2} es igual a {result}')
    return result


def resta(num_1, num_2):
    result = num_1 - num_2
    print(f'{num_1} - {num_2} es igual a {result}')
    return result


def multiplicacion(num_1, num_2):
    result=num_1 * num_2
    print(f'{num_1} * {num_2} es igual a {result}')
    return result
